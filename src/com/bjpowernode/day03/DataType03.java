package com.bjpowernode.day03;
/**
布尔类型(boolean)
  只有两个值：true、false
  定义一个boolean类型的变量：
  boolean b1 = true;
  boolean b2 = false;
*/
public class DataType03 {

	public static void main(String[] args) {
	   boolean b1 = true;
	   boolean b2 = false;
	   System.out.println(b1); // true
	   System.out.println(b2); // false
	   
	   System.out.println(100>120); // false
	   System.out.println(100<120); // true
	}
}









