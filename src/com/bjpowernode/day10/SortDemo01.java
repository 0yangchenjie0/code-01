package com.bjpowernode.day10;

/**
 * 对数组的元素从小到大排序
 * 冒泡排序
 * 相邻两个元素做比较，如果上一个元素大于下一个元素，则交换位置。
 */
public class SortDemo01 {

    public static void main(String[] args) {
        int[] arr = {5, 9, 1, 8, 2};
    }
}

// 外层第一次循环
// 5, 9, 1, 8, 2
// 5, 1, 9, 8, 2
// 5, 1, 8, 9, 2
// 5, 1, 8, 2, 9

// 外层第二次循环
// 1, 5, 8, 2, 9
// 1, 5, 8, 2, 9
// 1, 5, 2, 8, 9

// 外层第三次循环
// 1, 5, 2, 8, 9
// 1, 2, 5, 8, 9

// 外层第四次循环
// 1, 2, 5, 8, 9






